# Schema Validation
# -----------------

def validate_metadata_schema(feature_id: str, metadata: dict) -> bool:
    errs = sorted(
        _metadata_schema_validator.iter_errors(metadata),
        key=lambda e: list(e.absolute_path),
    )
    if errs:
        for err in errs:
            path = (
                " → ".join(str(p) for p in err.absolute_path)
                if err.absolute_path
                else "(root)"
            )
            log(f"❌ {feature_id}: {path}: {err.message}")
        return False

    return True


def _get_metadata_schema_validator():
    metadata_schema = json.loads(METADATA_SCHEMA_FILEPATH.read_text(encoding="utf-8"))
    _rewrite_remote_refs(metadata_schema)
    return jsonschema.Draft7Validator(metadata_schema)


def _rewrite_remote_refs(obj: object) -> None:
    """Replace the remote manifest $ref with the local file:// URI (in-place)."""
    if isinstance(obj, dict):
        if obj.get("$ref") == _REMOTE_MANIFEST_URL:
            obj["$ref"] = OSPKG_MANIFEST_SCHEMA_FILEPATH.as_uri()
        for v in obj.values():
            _rewrite_remote_refs(v)
    elif isinstance(obj, list):
        for item in obj:
            _rewrite_remote_refs(item)
    return


_metadata_schema_validator = _get_metadata_schema_validator()


# Dependency Manifest Generation
# ------------------------------

def generate_dependency_manifests(metadata: dict) -> dict[Path, str]:
    """Sync the dependencies/ directory for the given feature based on its metadata."""
    deps = metadata.get("_dependencies")
    if not deps:
        return {}

    dirpath = Path("dependencies")
    manifests = {}

    for lifecycle, groups in deps.items():
        for dep_name, dep_content in groups.items():
            dep_path = dirpath / lifecycle / f"{dep_name}.yaml"
            manifest = yaml.dump(dep_content, default_flow_style=False, sort_keys=False, allow_unicode=True)
            manifests[dep_path] = manifest
    return manifests


# Markdown Sanitation
# -------------------

def sanitize_markdown(metadata: dict) -> None:
    """Recursively process a value, stripping markdown from description fields."""

    metadata["description"] = _normalize_description(metadata["description"])

    if "options" in metadata:
        for option in metadata["options"].values():
            option["description"] = _normalize_description(option["description"])

    return


def _normalize_description(text: str) -> str:
    """Strip markdown and normalize whitespace for JSON output.

    Strips leading/trailing whitespace and collapses multiple consecutive
    blank lines to a single blank line, while preserving intentional
    paragraph structure.
    """
    text = _strip_markdown(text)
    lines = [line.rstrip() for line in text.splitlines()]
    # Collapse runs of blank lines
    result: list[str] = []
    prev_blank = False
    for line in lines:
        blank = not line.strip()
        if blank:
            if not prev_blank:
                result.append("")
            prev_blank = True
        else:
            result.append(line)
            prev_blank = False
    # Strip leading/trailing blank lines
    while result and not result[0]:
        result.pop(0)
    while result and not result[-1]:
        result.pop()
    return "\n".join(result)


def _strip_markdown(text: str) -> str:
    """Strip markdown formatting from a description string.

    Handles:
    - Images:   ![alt](url)        → alt
    - Links:    [text](url)        → text
    - Bold:     **text** / __text__ → text
    - Italic:   *text*              → text   (``_text_`` is intentionally left
                                              alone — too ambiguous with
                                              shell variables and identifiers)
    - HTML tags: <tag ...> / </tag> → (removed)
      Only real HTML is stripped: closing tags (</...>) and opening tags that
      carry attributes (contain whitespace).  Single-word angle-bracket tokens
      like <version>, <shell>, <home_dir> used as documentation placeholders
      are intentionally left untouched.

    Backtick code spans are intentionally preserved: they remain readable
    in plain text and are common in technical option descriptions.
    """
    if not text:
        return text
    # Images before links (avoid double-processing)
    text = re.sub(r"!\[([^\]]*)\]\([^)]*\)", r"\1", text)
    # Links
    text = re.sub(r"\[([^\]]*)\]\([^)]*\)", r"\1", text)
    # Bold
    text = re.sub(r"\*\*([^*]+)\*\*", r"\1", text)
    text = re.sub(r"__([^_]+)__", r"\1", text)
    # Italic (asterisk only)
    text = re.sub(r"\*([^*\n]+)\*", r"\1", text)
    # HTML tags: closing tags </foo> and opening tags with attributes <foo ...>
    # Single-word tokens like <version> or <shell> are NOT matched.
    text = re.sub(r"</[a-zA-Z][^>]*>", "", text)
    text = re.sub(r"<[a-zA-Z][^>]*\s[^>]*>", "", text)
    return text


# Metadata JSON Generation
# ------------------------

def generate_metadata_json(feature_id: str, metadata: dict) -> dict[Path, str]:
    """Generate devcontainer-feature.json content from parsed YAML data.

    Returns the JSON string (with trailing newline).
    """
    metadata_json_dict: dict = {
        "documentationURL": DOC_URL_TEMPLATE.format(feature_id=feature_id),
        "licenseURL": LICENSE_URL
    }

    for key, value in metadata.items():
        if key.startswith("_"):
            continue
        if key != "options":
            metadata_json_dict[key] = value
            continue

        options = {}
        for option_id, option_raw in value.items():
            option = {}
            for k, v in option_raw.items():
                if k == "type" and v == "array":
                    option[k] = "string"
                elif k in ("enum", "proposals"):
                    option[k] = [item["value"] for item in v]
                else:
                    option[k] = v
            options[option_id] = option
        metadata_json_dict[key] = options

    metadata_json = json.dumps(metadata_json_dict, sort_keys=True, indent=3, ensure_ascii=False) + "\n"
    return {Path("devcontainer-feature.json"): metadata_json}


# Install Script Generation
# -------------------------

def generate_install_script(feature_id: str, metadata: dict) -> dict[Path, str]:
    """Generate the full install.bash content for a feature.

    The generated content covers everything from the shebang (#!/usr/bin/env bash)
    through the argument-parsing block and default-variable initialisation, and is
    terminated by a single end marker:

        # END OF AUTOGENERATED BLOCK

    Everything after that marker in the file is hand-written implementation code
    and is never touched by this script.

    Options with ``type: array`` in metadata.yaml are treated as bash arrays:
    * CLI branch:      --flag <value>  (repeatable; each invocation appends)
    * env-var branch:  newline-delimited string split into an array
    * Defaults:        empty array  ()

    The ``type: array`` is serialised as ``type: string`` in devcontainer-feature.json
    (handled by metadata.py).
    """

    target_file = Path("install.bash")
    body_path = FEATURES_DIRPATH / feature_id / target_file

    if not body_path.exists():
        return {}

    body = body_path.read_text(encoding="utf-8").strip("\n")

    feature_name: str = metadata["name"]
    options: dict = metadata["options"]
    dependencies: dict | None = metadata.get("_dependencies")

    header = _generate_block(feature_name, options, dependencies)
    full_script = _shfmt_format(f"{header}\n\n{body}\n")
    return {target_file: full_script}


def _generate_block(feature_name: str, options: dict, dependencies: dict | None = None) -> str:
    """Return the full generated content from shebang to END marker (inclusive).

    The returned string ends with ``# END OF AUTOGENERATED BLOCK\\n``.
    If ``_dependencies.run`` contains a ``base`` key, a ``_base_deps__install``
    call is emitted as the last statement before the end marker.
    If ``_dependencies.build`` is non-empty, dep-group helper functions are emitted.
    """
    run_deps: dict = (dependencies or {}).get("run") or {}
    build_deps: dict = (dependencies or {}).get("build") or {}
    parts = [
        _render_template("preamble", FEATURE_NAME=feature_name),
        _section_usage(options),
        _section_arg_parse(options),
        _section_debug_flag(),
        _section_defaults(options),
        _section_validation(options),
        _section_unexport(options),
        _section_dep_helpers(run_deps, build_deps),
        _section_base_deps_call(run_deps),
        INSTALL_SCRIPT_HEADER_END_MARKER,
    ]
    return "\n\n".join(p for p in parts if p)


def _section_usage(options: dict) -> str:
    """Emit the __usage__() shell function."""
    entries: list[tuple[str, str, str, bool]] = []
    for key, opt in options.items():
        typ = opt["type"]
        flag_str = f"  {_opt_to_flag(key)} {_usage_type_hint(opt)}"
        has_default = "default" in opt
        default = opt.get("default", "")
        is_required = not has_default
        if typ == "boolean":
            disp_default = "true" if default else "false"
        elif typ == "array":
            disp_default = ""
        else:
            disp_default = str(default) if has_default and default is not None else ""
        desc = _first_sentence(opt.get("description", ""))
        entries.append((flag_str, desc, disp_default, is_required))

    help_flag = "  -h, --help"
    col_width = max((len(f) for f, *_ in entries), default=len(help_flag))
    col_width = max(col_width, len(help_flag))

    lines = ["__usage__() {", "  cat << 'EOF'", "Usage: install.bash [OPTIONS]", "", "Options:"]
    for (flag_str, desc, disp_default, is_required), _ in zip(entries, options.items()):
        padding = " " * (col_width - len(flag_str) + 2)
        if is_required:
            desc_field = f"{desc} (required)"
        elif disp_default:
            desc_field = f'{desc} (default: "{disp_default}")'
        else:
            desc_field = desc
        lines.append(f"{flag_str}{padding}{desc_field}")
    help_padding = " " * (col_width - len(help_flag) + 2)
    lines.append(f"{help_flag}{help_padding}Show this help")
    lines.extend(["EOF", "  return", "}"])
    return "\n".join(lines)


def _section_arg_parse(options: dict) -> str:
    """Emit the if/else/fi CLI-vs-env-var argument parsing block."""
    cli_inits: list[str] = []
    case_arms: list[str] = []
    env_reads: list[str] = []

    for key, opt in options.items():
        vname = _opt_to_var(key)
        typ = opt.get("type", "string")
        flag = _opt_to_flag(key)
        # Arrays are intentionally NOT pre-initialised: bash +=() on an unset variable
        # is safe, and pre-setting to () would fool the declare-based default check.
        if typ != "array":
            if "default" not in opt:
                cli_inits.append(f'  {vname}=""')
            else:
                cli_inits.append(f"  {vname}={_shell_val(opt['default'], typ)}")
        if typ == "array":
            case_arms.append(_render_template("case_arm_array", FLAG=flag, VAR=vname, KEY=key))
            env_reads.append(_render_template("env_read_array", VAR=vname, KEY=key))
        else:
            case_arms.append(_render_template("case_arm_scalar", FLAG=flag, VAR=vname, KEY=key))
            env_reads.append(_render_template("env_read_scalar", VAR=vname, KEY=key))

    lines = [
        'if [ "$#" -gt 0 ]; then',
        '  echo "ℹ️ Script called with arguments: $*" >&2',
    ]
    lines.extend(cli_inits)
    lines.extend(['  while [ "$#" -gt 0 ]; do', "    case $1 in"])
    lines.extend(case_arms)
    lines.extend([
        "      -h | --help)",
        "        __usage__",
        "        exit 0",
        "        ;;",
        "      --*)",
        "        echo \"⛔ Unknown option: '${1}'\" >&2",
        "        exit 1",
        "        ;;",
        "      *)",
        "        echo \"⛔ Unexpected argument: '${1}'\" >&2",
        "        exit 1",
        "        ;;",
        "    esac",
        "  done",
        "else",
        '  echo "ℹ️ Script called with no arguments. Read environment variables." >&2',
    ])
    lines.extend(env_reads)
    lines.append("fi")
    return "\n".join(lines)


def _section_debug_flag() -> str:
    return '[[ "${DEBUG:-}" == true ]] && set -x'


def _section_defaults(options: dict) -> str:
    """Emit the '# Apply defaults.' block."""
    blocks = ["# Apply defaults."]
    for key, opt in options.items():
        vname = _opt_to_var(key)
        typ = opt.get("type", "string")
        if "default" not in opt:
            continue
        if typ == "array":
            raw_default = opt["default"]
            if raw_default == "" or raw_default is None:
                blocks.append(_render_template("default_array_empty", VAR=vname, KEY=key))
            else:
                # Embed the default as an ANSI-C quoted string so newlines are preserved.
                escaped = (
                    str(raw_default)
                    .replace("\\", "\\\\")
                    .replace("'", "\\'")
                    .replace("\n", "\\n")
                    .replace("\r", "\\r")
                )
                disp = ", ".join(str(raw_default).splitlines())
                blocks.append(_render_template("default_array_value", VAR=vname, ESCAPED=escaped, KEY=key, DISP=disp))
        else:
            rhs = _shell_val(opt["default"], typ)
            if typ == "boolean":
                disp = "true" if opt["default"] else "false"
            elif opt["default"] == "" or opt["default"] is None:
                disp = ""
            else:
                disp = str(opt["default"])
            blocks.append(_render_template("default_scalar", VAR=vname, RHS=rhs, KEY=key, DISP=disp))
    return "\n".join(blocks)


def _section_validation(options: dict) -> str:
    """Emit required-argument checks and enum validation. Returns '' if neither applies."""
    required_opts = [(k, v) for k, v in options.items() if "default" not in v]
    enum_opts = [(k, v) for k, v in options.items() if v.get("enum")]
    if not required_opts and not enum_opts:
        return ""

    parts: list[str] = []
    if required_opts:
        checks: list[str] = []
        for key, opt in required_opts:
            vname = _opt_to_var(key)
            tpl = "validation_required_array" if opt.get("type", "string") == "array" else "validation_required_scalar"
            checks.append(_render_template(tpl, VAR=vname, KEY=key))
        parts.append("# Check required arguments.\n" + "\n".join(checks))

    if enum_opts:
        validations: list[str] = []
        for key, opt in enum_opts:
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            values = [item["value"] if isinstance(item, dict) else str(item) for item in opt["enum"]]
            expected = ", ".join(repr(v) if v == "" else v for v in values)
            pattern = " | ".join("''" if v == "" else v for v in values)
            tpl = "validation_enum_array" if typ == "array" else "validation_enum_scalar"
            validations.append(_render_template(tpl, VAR=vname, KEY=key, PATTERN=pattern, EXPECTED=expected))
        parts.append("# Validate enum options.\n" + "\n".join(validations))

    return "\n\n".join(parts)


def _section_unexport(options: dict) -> str:
    """Emit the 'declare +x' unexport line for all scalar option variables."""
    scalar_vars = [
        _opt_to_var(k) for k, v in options.items() if v.get("type", "string") != "array"
    ]
    if not scalar_vars:
        return ""
    return "\n".join([
        "# Unexport option variables — values remain accessible in this script but",
        "# are not inherited by child processes.",
        "declare +x " + " ".join(scalar_vars),
    ])


def _section_dep_helpers(run_deps: dict, build_deps: dict) -> str:
    """Emit _<group>_deps__install() / _<group>_deps__cleanup() helper functions."""
    if not run_deps and not build_deps:
        return ""
    blocks = ["# ── dep-group helpers (generated) ──────────────────────────────────────────────"]
    for group_name in run_deps:
        blocks.append(_render_template("dep_helper_run", SAFE=group_name.replace("-", "_"), GROUP=group_name))
    for group_name in build_deps:
        blocks.append(_render_template("dep_helper_build", SAFE=group_name.replace("-", "_"), GROUP=group_name))
    return "\n".join(blocks)


def _section_base_deps_call(run_deps: dict) -> str:
    """Emit the root-guarded _base_deps__install() call, or '' if no base group."""
    if "base" not in run_deps:
        return ""
    return _render_template("base_deps_call")


def _shfmt_format(text: str) -> str:
    """Format a bash script fragment through shfmt, respecting .editorconfig.

    Writes to a temp file in the repo root so shfmt finds the .editorconfig
    style settings.  Returns *text* unchanged if shfmt is not on PATH.
    """
    with tempfile.NamedTemporaryFile(
        mode="w",
        suffix=".sh",
        dir=REPO_DIRPATH,
        delete=False,
        encoding="utf-8",
    ) as tmp:
        tmp.write(text)
        tmp_path = Path(tmp.name)
    try:
        result = subprocess.run(
            ["shfmt", "-w", str(tmp_path)],
            capture_output=True,
            cwd=REPO_DIRPATH,
        )
        if result.returncode == 0:
            return tmp_path.read_text(encoding="utf-8")
        return text
    except FileNotFoundError:
        return text
    finally:
        tmp_path.unlink(missing_ok=True)


def _first_sentence(text: str) -> str:
    """Return the first non-empty line of a description."""
    for line in text.splitlines():
        line = line.strip()
        if line:
            return line
    return text.strip()


def _opt_to_var(key: str) -> str:
    """Convert a schema-validated option key to its installer env-var name.

    Option keys are constrained by features/metadata.schema.json to start with an
    ASCII letter and then contain only ASCII letters, digits, underscores, or
    hyphens. Given that contract, env-var generation is just uppercase plus
    '-' -> '_'.
    """
    return key.upper().replace("-", "_")


def _opt_to_flag(key: str) -> str:
    """Convert an option key to its --flag form."""
    return f"--{key}"


def _shell_val(default, typ: str) -> str:
    """Format a YAML default value as a shell assignment RHS (non-array types only)."""
    if typ == "boolean":
        return "true" if default else "false"
    if default == "" or default is None:
        return '""'
    return f'"{default}"'


def _usage_type_hint(opt: dict) -> str:
    """Return a concise type hint for the --help usage line."""
    typ = opt["type"]
    enum_raw = opt.get("enum", [])
    enum_values = [item["value"] if isinstance(item, dict) else str(item) for item in enum_raw]
    if typ == "boolean":
        return "{true,false}"
    if typ == "array":
        if enum_values:
            return "{" + "|".join(enum_values) + "}  (repeatable)"
        return "<value>  (repeatable)"
    if enum_values:
        return "{" + "|".join(enum_values) + "}"
    return "<value>"


def _render_template(name: str, **subs: str) -> str:
    """Load a shell snippet template from features/_templates/ and substitute @@KEY@@ placeholders."""
    template = _install_script_templates[name]
    for key, val in subs.items():
        template = template.replace(f"@@{key}@@", val)
    return template


def _read_templates() -> dict[str, str]:
    """Read all templates from the _templates directory and return a mapping of template name to content."""
    templates = {}
    for template_path in INSTALL_SCRIPT_TEMPLATES_DIRPATH.glob("*.tmpl"):
        template_name = template_path.name.removesuffix(".sh.tmpl")
        templates[template_name] = template_path.read_text(encoding="utf-8").rstrip("\n")
    return templates


_install_script_templates = _read_templates()


