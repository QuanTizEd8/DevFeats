def run(check_only: bool = False):

    n_features: int = 0
    n_failures: dict[str, int] = {
        "read": 0,
        "augmentation": 0,
        "schema validation": 0,
        "sync": 0,
    }

    lib_files = gather_lib_files()
    bootstrap_file = gather_bootstrap()

    for feature_dirpath in sorted(FEATURES_DIRPATH.iterdir()):

        if not feature_dirpath.is_dir() or feature_dirpath.name[0] in (".", "_"):
            continue

        n_features += 1
        feature_id = feature_dirpath.name
        output_files: dict[Path, str] = {}

        metadata = read_metadata(feature_id)
        if metadata is None:
            n_failures["read"] += 1
            continue

        augmentation_ok = augment_metadata(feature_id, metadata)
        if not augmentation_ok:
            n_failures["augmentation"] += 1
            continue

        validation_ok = validate_metadata_schema(feature_id, metadata)
        if not validation_ok:
            n_failures["schema validation"] += 1
            continue

        dependency_manifests = generate_dependency_manifests(metadata)
        output_files.update(dependency_manifests)

        sanitize_markdown(metadata)

        metadata_json = generate_metadata_json(feature_id, metadata)
        output_files.update(metadata_json)

        install_script = generate_install_script(feature_id, metadata)
        output_files.update(install_script)
        output_files.update(lib_files)
        output_files.update(bootstrap_file)
        output_files.update(gather_feature_files(feature_id))

        sync_ok = sync_source_files(feature_id, output_files, check_only=check_only)
        if not sync_ok:
            n_failures["sync"] += 1

    log("Final results:")
    if any(n_failures.values()):
        n_failures_total = sum(n_failures.values())
        log(f"\n{n_failures_total}/{n_features} feature(s) failed validation.")
        return 1

    log(f"\n✅  All {n_features} features passed.")
    return 0


# Metadata Reading
# ----------------

def read_metadata(feature_id: str) -> dict | None:
    """Read and parse the metadata.yaml for the given feature ID."""
    metadata_filepath = FEATURES_DIRPATH / feature_id / "metadata.yaml"
    if not metadata_filepath.is_file():
        log(f"⛔ {feature_id}: metadata.yaml not found")
        return None

    try:
        data = yaml.safe_load(metadata_filepath.read_text(encoding="utf-8"))
    except yaml.YAMLError as exc:
        log(f"❌ {feature_id}: YAML parse error: {exc}")
        return None

    if not isinstance(data, dict):
        log(f"❌ {feature_id}: metadata.yaml does not contain a mapping (got {type(data).__name__})")
        return None

    return data


# Metadata Augmentation
# ---------------------

def augment_metadata(feature_id: str, metadata: dict) -> bool:
    """Generate the full options dict for a feature.

    Add derived options from features/derived-options.yaml,
    conditionally applying options with _apply_when
    based on the feature's full metadata dict.
    """

    options: dict = metadata.get("options", {})
    for option_id, option_def in _derived_options.items():
        if option_id in options:
            log(f"⛔ {feature_id}: option '{option_id}' is a derived option and cannot be manually defined in metadata.yaml")
            return False
        should_apply = (
            _evaluate_condition(option_def["_apply_when"], metadata)
            if "_apply_when" in option_def else True
        )
        if should_apply:
            options[option_id] = {k: v for k, v in option_def.items() if not k.startswith("_")}
    metadata["options"] = options
    return True


def _evaluate_condition(apply_when: dict, data: dict) -> bool:
    """Evaluate an _apply_when condition against the feature's full metadata dict."""
    jsonpath = apply_when["jsonpath"]
    exists, value = _resolve_jsonpath(jsonpath, data)
    condition = apply_when["condition"]
    if condition == "exists":
        return exists
    if condition == "not_exists":
        return not exists
    if condition == "equals":
        expected = apply_when["value"]
        return exists and value == expected
    if condition == "not_equals":
        expected = apply_when["value"]
        return not exists or value != expected
    raise ValueError(f"Unsupported condition: {condition}")


def _resolve_jsonpath(jsonpath: str, data: dict) -> tuple[bool, object]:
    """Resolve a simple JSONPath expression against the feature metadata dict.

    Supported JSONPath syntax:
    - Root object: $
    - Dot notation for object properties: $.property

    Returns
    -------
    exists
        Whether the path exists in the metadata dict.
    value
        The value at the path if it exists, or None if it does not exist.
    """
    if not jsonpath.startswith("$."):
        raise ValueError(f"Unsupported JSONPath expression: {jsonpath}")
    path_parts = jsonpath[2:].split(".")
    current = data
    for part in path_parts:
        if not isinstance(current, dict) or part not in current:
            return False, None
        current = current[part]
    return True, current


