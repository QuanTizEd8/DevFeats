class TestGroupFields:
    """Tests for group-level fields: post, on_failure, and pre."""

    def test_post_generates_cleanup(self) -> None:
        """Post field generates a _cleanup() function and trap."""
        yaml_text = (
            "t:\n  post: 'rm -f /tmp/x'\n  checks:\n    - title: t\n      cmd: true\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert "_cleanup() {" in out
        assert "  rm -f /tmp/x" in out
        assert "trap _cleanup EXIT" in out

    def test_post_trap_before_test_failure_diag(self) -> None:
        """Trap _cleanup EXIT appears before _test_failure_diagnostics."""
        yaml_text = (
            "t:\n"
            "  post: 'cleanup body'\n"
            "  on_failure: 'diag body'\n"
            "  checks:\n    - title: t\n      cmd: true\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        trap_pos = out.index("trap _cleanup EXIT")
        diag_pos = out.index("_test_failure_diagnostics()")
        assert trap_pos < diag_pos

    def test_on_failure_generates_diag_function(self) -> None:
        """on_failure field generates a _test_failure_diagnostics() function."""
        yaml_text = (
            "t:\n"
            "  on_failure: 'cat /var/log/foo'\n"
            "  checks:\n    - title: t\n      cmd: true\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert "_test_failure_diagnostics() {" in out
        assert "  cat /var/log/foo" in out

    def test_pre_content_verbatim(self) -> None:
        """Pre content is emitted verbatim before check calls."""
        yaml_text = (
            "t:\n"
            "  pre: '_VAR=\"$(command -v yq)\"'\n"
            "  checks:\n    - title: t\n      cmd: true\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert '_VAR="$(command -v yq)"' in out

    def test_pre_appears_before_checks(self) -> None:
        """Pre content appears before any check calls in the output."""
        yaml_text = "t:\n  pre: '_X=1'\n  checks:\n    - title: t\n      cmd: true\n"
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        pre_pos = out.index("_X=1")
        check_pos = out.index('check "t"')
        assert pre_pos < check_pos

    def test_no_post_no_cleanup(self) -> None:
        """Absence of post means no _cleanup function is generated."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        assert "_cleanup" not in out

    def test_no_on_failure_no_diag(self) -> None:
        """Absence of on_failure means no diagnostics function is generated."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        assert "_test_failure_diagnostics" not in out


