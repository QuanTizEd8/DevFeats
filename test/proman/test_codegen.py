class TestDebugOnFail:
    """Tests for debug and on_fail field rendering."""

    def test_debug_emitted_before_check(self) -> None:
        """Debug output is emitted before its associated check call."""
        yaml_text = (
            "t:\n"
            "  checks:\n"
            "    - title: 'check path'\n"
            "      cmd: 'test -f /x'\n"
            "      debug: 'echo \"=== /x ===\"'\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        debug_pos = out.index('echo "=== /x ==="')
        check_pos = out.index('check "check path"')
        assert debug_pos < check_pos

    def test_on_fail_snapshot_and_guard(self) -> None:
        """on_fail emits a snapshot variable and a conditional guard block."""
        yaml_text = (
            "t:\n"
            "  checks:\n"
            "    - title: 'check x'\n"
            "      cmd: 'test -f /x'\n"
            "      on_fail: 'ls -la /x || true'\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert '_df0="$_TEST_FAIL"' in out
        assert '[ "$_TEST_FAIL" -eq "$_df0" ] || {' in out
        assert "ls -la /x || true" in out

    def test_on_fail_index_increments(self) -> None:
        """Two checks with on_fail use distinct index variables."""
        yaml_text = (
            "t:\n"
            "  checks:\n"
            "    - title: 'a'\n      cmd: 'true'\n      on_fail: 'echo a'\n"
            "    - title: 'b'\n      cmd: 'true'\n"
            "    - title: 'c'\n      cmd: 'true'\n      on_fail: 'echo c'\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert '_df0="$_TEST_FAIL"' in out
        assert '_df2="$_TEST_FAIL"' in out
        # Index 1 has no on_fail.
        assert '_df1="$_TEST_FAIL"' not in out


