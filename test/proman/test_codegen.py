from __future__ import annotations

from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from pathlib import Path

import pytest
import yaml
from proman.test.codegen import _render_group, generate_tests

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _load_group(yaml_text: str) -> tuple[str, dict]:
    data = yaml.safe_load(yaml_text)
    test_id, group = next(iter(data.items()))
    return test_id, group


# ---------------------------------------------------------------------------
# Header / boilerplate
# ---------------------------------------------------------------------------


class TestHeader:
    """Tests for the header and boilerplate section of generated .sh files."""

    def test_shebang_and_set_e(self) -> None:
        """Shebang and set -e appear in generated output."""
        _, g = _load_group("my_test:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("my_test", g)
        assert out.startswith("#!/usr/bin/env bash\n")
        assert "set -e\n" in out

    def test_source_line(self) -> None:
        """Source line for the test lib is present."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        assert ". dev-container-features-test-lib\n" in out

    def test_autogenerated_marker(self) -> None:
        """AUTO-GENERATED marker is present."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        assert "# AUTO-GENERATED from checks.yaml" in out

    def test_no_description(self) -> None:
        """No description leaves no comment before the AUTO-GENERATED marker."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        # No extra comment lines before the AUTO-GENERATED marker.
        lines = out.splitlines()
        assert lines[0] == "#!/usr/bin/env bash"
        assert lines[1] == "# AUTO-GENERATED from checks.yaml \u2014 DO NOT EDIT"

    def test_description_appears_before_marker(self) -> None:
        """Description comment precedes the AUTO-GENERATED marker."""
        _, g = _load_group(
            "t:\n  description: 'Verify yq is installed.'\n"
            "  checks:\n    - title: t\n      cmd: true\n"
        )
        out = _render_group("t", g)
        lines = out.splitlines()
        assert lines[1] == "# Verify yq is installed."
        assert lines[2] == "# AUTO-GENERATED from checks.yaml \u2014 DO NOT EDIT"

    def test_multiline_description(self) -> None:
        """Multi-line description is emitted as comment lines."""
        _, g = _load_group(
            "t:\n  description: |\n    Line one.\n    Line two.\n"
            "  checks:\n    - title: t\n      cmd: true\n"
        )
        out = _render_group("t", g)
        assert "# Line one.\n# Line two.\n" in out

    def test_ends_with_report_results(self) -> None:
        """Output ends with reportResults."""
        _, g = _load_group("t:\n  checks:\n    - title: t\n      cmd: true\n")
        out = _render_group("t", g)
        assert out.endswith("\nreportResults\n")


