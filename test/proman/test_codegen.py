class TestEdgeCases:
    """Edge-case and parametrize tests for check rendering."""

    def test_empty_debug_field_omitted(self) -> None:
        """Empty debug field does not emit extra output before the check."""
        yaml_text = "t:\n  checks:\n    - title: t\n      cmd: true\n      debug: ''\n"
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        # Empty debug must not emit a bare newline before the check
        check_idx = out.index('check "t"')
        # The character immediately before check should be a newline (section separator)
        assert out[check_idx - 1] == "\n"

    def test_empty_on_fail_field_omitted(self) -> None:
        """Empty on_fail field does not emit a snapshot variable or guard."""
        yaml_text = (
            "t:\n  checks:\n    - title: t\n      cmd: true\n      on_fail: ''\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert "_df0" not in out

    @pytest.mark.parametrize(
        ("title", "expected"),
        [
            ("simple", '"simple"'),
            ('with "quotes"', '"with \\"quotes\\""'),
        ],
    )
    def test_title_quoting(self, title: str, expected: str) -> None:
        """Titles are wrapped in double quotes with interior quotes escaped."""
        yaml_text = f"t:\n  checks:\n    - title: '{title}'\n      cmd: true\n"
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert expected in out
