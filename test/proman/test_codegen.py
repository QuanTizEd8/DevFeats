class TestCheckItem:
    """Tests for individual check/fail_check item rendering."""

    def test_simple_check(self) -> None:
        """Simple check call is emitted correctly."""
        _, g = _load_group(
            "t:\n  checks:\n    - title: 'yq available'\n      cmd: 'command -v yq'\n"
        )
        out = _render_group("t", g)
        assert 'check "yq available" command -v yq\n' in out

    def test_kind_fail(self) -> None:
        """fail_check is emitted for kind=fail."""
        _, g = _load_group(
            "t:\n  checks:\n"
            "    - title: 'cmd fails'\n      cmd: 'false'\n      kind: fail\n"
        )
        out = _render_group("t", g)
        assert 'fail_check "cmd fails" false\n' in out

    def test_kind_check_explicit(self) -> None:
        """Explicit kind=check still emits check."""
        _, g = _load_group(
            "t:\n  checks:\n    - title: 'ok'\n      cmd: 'true'\n      kind: check\n"
        )
        out = _render_group("t", g)
        assert 'check "ok" true\n' in out

    def test_title_with_double_quotes_escaped(self) -> None:
        """Double quotes in a title are escaped."""
        _, g = _load_group(
            "T:\n  checks:\n    - title: 'a \"quoted\" title'\n      cmd: true\n"
        )
        out = _render_group("T", g)
        assert 'check "a \\"quoted\\" title" true\n' in out

    def test_multiline_cmd_backslash_continuation(self) -> None:
        """Multiline cmd uses backslash continuation."""
        yaml_text = (
            "t:\n"
            "  checks:\n"
            "    - title: 'multi'\n"
            "      cmd: |\n"
            "        bash -c \\\n"
            "          'test -f /a'\n"
        )
        _, g = _load_group(yaml_text)
        out = _render_group("t", g)
        assert 'check "multi" \\\n' in out
        assert "  bash -c \\\n" in out


