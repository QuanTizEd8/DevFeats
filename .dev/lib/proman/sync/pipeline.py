"""sync-src.py — Assembles each feature's src/ directory from features/ + lib/ + features/bootstrap.sh:

  - features/*/metadata.yaml → src/*/devcontainer-feature.json
  - features/*/metadata.yaml → src/*/dependencies/*.yaml
  - features/*/install.bash  → src/*/install.bash (autogenerated header prepended)
  - lib/                     → src/*/_lib/
  - features/bootstrap.sh    → src/*/install.sh
  - features/*/files/        → src/*/files/ (copied, not symlinked)

Usage:
  python3 scripts/sync-src.py           # sync all features
  python3 scripts/sync-src.py --check   # verify src/ is up to date (exits non-zero if stale)
"""

