"""Install script generation for devcontainer features."""

from __future__ import annotations

import subprocess
import tempfile
from dataclasses import dataclass, field
from pathlib import Path

import pyserials

from proman.config import load as load_config
from proman.git import git_owner_repo


@dataclass
class _DiscoveryParams:
    stem: str
    var_prefix: str
    bin_dir: str
    var_discovery: str
    var_runtime_path: str
    bins_str: str
    var_symlinks: str
    var_exports: str
    marker: str
    first_bin: str
    cmd_var: str
    skip_symlink: bool = False
    skip_exports: bool = False
    symlink_root: str = "/usr/local/bin"
    symlink_nonroot: str = field(default="${HOME}/.local/bin")


class InstallScriptGenerator:
    """Generate install.bash header blocks from feature metadata."""

    def __init__(self) -> None:
        self._config = load_config()
        self._features_dirpath = self._config.absolute_path("path.features")
        self._template = self._config.absolute_path(
            "path.install_script_template"
        ).read_text()
        self._repo_dirpath = self._config.root_path
        return

    def generate(self, metadata: dict) -> dict[Path, str]:
        """Generate the full install.bash content for a feature.

        The generated content covers everything from the shebang (#!/usr/bin/env bash)
        through the argument-parsing block and default-variable initialisation, and is
        terminated by a single end marker:

            # END OF AUTOGENERATED BLOCK

        Everything after that marker in the file is hand-written implementation code
        and is never touched by this script.

        Options with ``type: array`` in metadata.yaml are treated as bash arrays:

        * CLI branch:      --flag <value>  (repeatable; each invocation appends)
        * env-var branch:  newline-delimited string split into an array
        * Defaults:        empty array  ()

        The ``type: array`` is serialised as ``type: string`` in
        devcontainer-feature.json (handled by metadata.py).
        """
        target_file = Path("install.bash")
        body_path = self._features_dirpath / metadata["id"] / target_file
        body = body_path.read_text(encoding="utf-8").strip("\n")
        header = self._generate_block(metadata=metadata)
        full_script = self._shfmt_format(f"{header}\n\n{body}\n")
        return {target_file: full_script}

    def _generate_block(self, metadata: dict) -> str:
        r"""Return the full generated content from shebang to END marker (inclusive).

        The returned string ends with ``# END OF AUTOGENERATED BLOCK\\n``.
        If ``_dependencies.run`` contains a ``base`` key, a ``_base_deps__install``
        call is emitted as the last statement before the end marker.
        If ``_dependencies.build`` is non-empty, dep-group helper functions are emitted.
        """
        feature_id: str = metadata["id"]
        options: dict = metadata["options"]
        dependencies: dict = metadata.get("_dependencies", {})
        prefix_groups: dict = metadata.get("_prefix_groups", {})

        run_deps: dict = dependencies.get("run", {})
        build_deps: dict = dependencies.get("build", {})

        script_parts: dict[str, object] = {
            "usage_options": self._generate_usage_options(options),
            "argparse": self._generate_argparse(options),
            "env_var_assignments": self._generate_env_var_assignments(
                metadata.get("_env_vars", {})
            ),
            "dependency_install_functions": self._generate_dependency_install_functions(
                run_deps, build_deps
            ),
            "dependency_install_calls": self._generate_dependency_install_calls(
                run_deps, build_deps
            ),
            "prefix_resolver_functions": self._generate_prefix_resolver_functions(
                prefix_groups, feature_id
            ),
            "prefix_post_install_body": self._generate_prefix_post_install_body(
                prefix_groups, feature_id
            ),
            "prefix_resolver_calls": self._generate_prefix_resolver_calls(
                prefix_groups
            ),
            "system_requirements_guard": self._generate_system_requirements_guard(
                metadata.get("_system_requirements", {})
            ),
        }
        template_data = metadata | {"_script": script_parts}
        try:
            script: str = pyserials.update.TemplateFiller().fill(
                data=template_data,
                template=self._template,
            )
        except Exception as e:
            raise ValueError(f"Error rendering template: {e}") from e

        return script

    @staticmethod
    def _generate_env_var_assignments(env_vars: dict) -> str:
        """Emit one ``_KEY="value"`` line per entry in metadata ``_env_vars``.

        Each snake_case key ``foo_bar`` becomes bash variable ``_FOO_BAR``.
        Values are double-quoted so ``${HOME}`` and similar expansions still
        work at runtime.
        """
        if not isinstance(env_vars, dict) or not env_vars:
            return ""

        lines: list[str] = []
        for key in sorted(env_vars):
            value = env_vars[key]
            if not isinstance(value, str):
                msg = (
                    f"_env_vars['{key}'] must be a string after metadata fill"
                    f" (got {type(value).__name__})"
                )
                raise TypeError(msg)
            var_name = f"_{key.upper()}"
            escaped = value.replace("\\", "\\\\").replace('"', '\\"')
            lines.append(f'{var_name}="{escaped}"')
        return "\n".join(lines)

    def _generate_usage_options(self, options: dict) -> str:
        """Emit the __usage__() shell function."""
        entries: list[tuple[str, str, str, bool]] = []
        for key, opt in options.items():
            typ = opt["type"]
            flag_str = f"  {_opt_to_flag(key)} {_usage_type_hint(opt)}"
            has_default = "default" in opt
            default = opt.get("default", "")
            is_required = not has_default
            if typ == "boolean":
                disp_default = "true" if default else "false"
            elif typ == "array":
                disp_default = ""
            else:
                disp_default = (
                    str(default) if has_default and default is not None else ""
                )
            desc = _first_sentence(opt.get("description", ""))
            entries.append((flag_str, desc, disp_default, is_required))
        entries.append(("  -h, --help", "Show this help and exit.", "", False))

        col_width = max((len(f) for f, *_ in entries))
        lines = []
        for (flag_str, desc, disp_default, is_required), _ in zip(
            entries,
            options.items(),
            strict=False,
        ):
            padding = " " * (col_width - len(flag_str) + 2)
            if is_required:
                desc_field = f"{desc} (required)"
            elif disp_default:
                desc_field = f'{desc} (default: "{disp_default}")'
            else:
                desc_field = desc
            lines.append(f"{flag_str}{padding}{desc_field}")

        return "\n".join(lines)

    def _generate_argparse(self, options: dict) -> dict[str, str]:
        """Emit the if/else/fi CLI-vs-env-var argument parsing block."""
        cli_inits: list[str] = []
        case_arms: list[str] = []
        env_reads: list[str] = []

        case_arm_indent = 8
        env_read_indent = 4

        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            flag = _opt_to_flag(key)
            # Arrays are intentionally NOT pre-initialised: bash +=() on an unset
            # variable is safe, and pre-setting to () would fool the declare-based
            # default check.
            if typ != "array":
                if "default" not in opt:
                    cli_inits.append(f'    {vname}=""')
                else:
                    cli_inits.append(f"    {vname}={_shell_val(opt['default'], typ)}")
            if typ == "array":
                case_arms.append(
                    self._render_inline_template(
                        _ARGPARSE_CASE_ARM_ARRAY,
                        case_arm_indent,
                        flag=flag,
                        var=vname,
                        key=key,
                    ),
                )
                env_reads.append(
                    self._render_inline_template(
                        _ARGPARSE_ENV_READ_ARRAY,
                        env_read_indent,
                        var=vname,
                        key=key,
                    ),
                )
            else:
                case_arms.append(
                    self._render_inline_template(
                        _ARGPARSE_CASE_ARM_SCALAR,
                        case_arm_indent,
                        flag=flag,
                        var=vname,
                        key=key,
                    ),
                )
                env_reads.append(
                    self._render_inline_template(
                        _ARGPARSE_ENV_READ_SCALAR,
                        env_read_indent,
                        var=vname,
                        key=key,
                    ),
                )

        out = {
            "cli_inits": "\n".join(cli_inits),
            "case_arms": "\n".join(case_arms),
            "env_reads": "\n".join(env_reads),
            "defaults": self._generate_argparse_defaults(options),
            "validations": self._generate_argparse_validations(options),
            "unexports": self._generate_argparse_unexports(options),
        }
        return out

    def _generate_argparse_defaults(self, options: dict) -> str:
        """Emit the '# Apply defaults.' block."""
        blocks = []
        dynamic_blocks = []
        indent = 2
        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            if "default" not in opt:
                continue
            if typ == "array":
                raw_default = opt["default"]
                if raw_default == "" or raw_default is None:
                    blocks.append(
                        self._render_inline_template(
                            _ARGPARSE_DEFAULT_ARRAY_EMPTY,
                            indent,
                            var=vname,
                            key=key,
                        ),
                    )
                else:
                    # Embed the default as an ANSI-C quoted string so newlines
                    # are preserved.
                    escaped = (
                        str(raw_default)
                        .replace("\\", "\\\\")
                        .replace("'", "\\'")
                        .replace("\n", "\\n")
                        .replace("\r", "\\r")
                    )
                    disp = ", ".join(str(raw_default).splitlines())
                    blocks.append(
                        self._render_inline_template(
                            _ARGPARSE_DEFAULT_ARRAY_VALUE,
                            indent,
                            var=vname,
                            escaped=escaped,
                            key=key,
                            disp=disp,
                        ),
                    )
            else:
                rhs = _shell_val(opt["default"], typ)
                if typ == "boolean":
                    disp = "true" if opt["default"] else "false"
                elif opt["default"] == "" or opt["default"] is None:
                    disp = ""
                else:
                    disp = str(opt["default"])
                blocks.append(
                    self._render_inline_template(
                        _ARGPARSE_DEFAULT_SCALAR,
                        indent,
                        var=vname,
                        rhs=rhs,
                        key=key,
                        disp=disp,
                    ),
                )
                dynamic_default = opt.get("_default")
                if dynamic_default is not None:
                    dynamic_blocks.append(
                        self._render_inline_template(
                            _ARGPARSE_DEFAULT_DYNAMIC,
                            indent,
                            var=vname,
                            rhs=f'"{dynamic_default}"',
                            key=key,
                        ),
                    )

        return "\n".join(blocks + dynamic_blocks)

    def _generate_argparse_validations(self, options: dict) -> str:
        """Emit boolean, integer, enum, and path validation blocks.

        Returns '' if none apply.
        """
        indent = 2
        validations: list[str] = []
        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            if typ == "boolean":
                validations.append(
                    self._render_inline_template(
                        _ARGPARSE_VALIDATION_ENUM_SCALAR,
                        indent,
                        var=vname,
                        key=key,
                        pattern="true | false",
                        expected="true, false",
                    ),
                )
            elif typ == "integer":
                validations.append(
                    self._render_inline_template(
                        _ARGPARSE_VALIDATION_INTEGER,
                        indent,
                        var=vname,
                        key=key,
                    ),
                )
                minimum = opt.get("_minimum")
                if minimum is not None:
                    validations.append(
                        self._render_inline_template(
                            _ARGPARSE_VALIDATION_INTEGER_MIN,
                            indent,
                            var=vname,
                            key=key,
                            minimum=str(minimum),
                        ),
                    )
                maximum = opt.get("_maximum")
                if maximum is not None:
                    validations.append(
                        self._render_inline_template(
                            _ARGPARSE_VALIDATION_INTEGER_MAX,
                            indent,
                            var=vname,
                            key=key,
                            maximum=str(maximum),
                        ),
                    )
            elif opt.get("enum"):
                values = [
                    item["value"] if isinstance(item, dict) else str(item)
                    for item in opt["enum"]
                ]
                expected = ", ".join(repr(v) if v == "" else v for v in values)
                pattern = " | ".join("''" if v == "" else v for v in values)
                tpl = (
                    _ARGPARSE_VALIDATION_ENUM_ARRAY
                    if typ == "array"
                    else _ARGPARSE_VALIDATION_ENUM_SCALAR
                )
                validations.append(
                    self._render_inline_template(
                        tpl,
                        indent,
                        var=vname,
                        key=key,
                        pattern=pattern,
                        expected=expected,
                    ),
                )
            path_spec = opt.get("_path")
            if path_spec is not None and typ != "array":
                ops = [path_spec] if isinstance(path_spec, str) else path_spec
                for op in ops:
                    validations.append(
                        self._render_inline_template(
                            _ARGPARSE_VALIDATION_PATH,
                            indent,
                            var=vname,
                            key=key,
                            test=_negate_path_test(op),
                            op=op,
                        ),
                    )
        return "\n".join(validations)

    def _generate_argparse_unexports(self, options: dict) -> str:
        """Emit the 'declare +x' unexport line for all scalar option variables."""
        scalar_vars = [
            _opt_to_var(k)
            for k, v in options.items()
            if v.get("type", "string") != "array"
        ]
        return " ".join(scalar_vars)

    def _generate_prefix_resolver_functions(
        self, prefix_groups: dict, feature_id: str
    ) -> str:
        """Emit per-group resolver functions and activation stubs."""
        if not prefix_groups:
            return ""

        fn_blocks: list[str] = []

        for group_id, group_cfg in prefix_groups.items():
            prefix_cfg: dict = group_cfg.get("prefix", {})
            activation_cfg: dict | None = group_cfg.get("activation")

            option_name: str | None = group_cfg.get("option_name")
            default_root: str = prefix_cfg.get("root", "/usr/local")
            default_nonroot: str = prefix_cfg.get("nonroot", "${HOME}/.local")

            stem = option_name or (f"{group_id}_prefix" if group_id else "prefix")
            var_prefix = _opt_to_var(stem)
            optname_disp = option_name or stem

            safe_id = group_id.replace("-", "_")
            if option_name:
                fn_resolve = f"resolve_{option_name}"
            elif group_id:
                fn_resolve = f"resolve_{safe_id}_prefix"
            else:
                fn_resolve = "resolve_prefix"
            fn_activation_snippet = f"{stem}_activation_snippet"

            _scope_block = (
                _TMPL_SCOPE_SNAPSHOT.format(var_prefix=var_prefix)
                if activation_cfg
                else ""
            )

            fn_blocks.append(
                self._render_inline_template(
                    _TMPL_PREFIX_RESOLVER,
                    0,
                    func_name=fn_resolve,
                    prefix_var=var_prefix,
                    opt_name=optname_disp,
                    resolution_block=_make_resolution_block(
                        var_prefix,
                        default_root,
                        default_nonroot,
                        prefix_cfg.get("platform_overrides", []),
                    ),
                    scope_block=_scope_block,
                )
            )

            if activation_cfg:
                fn_blocks.append(
                    self._render_inline_template(
                        _TMPL_ACTIVATION_STUB, 0, func_name=fn_activation_snippet
                    )
                )

        return "\n\n".join(fn_blocks)

    def _generate_prefix_post_install_body(
        self, prefix_groups: dict, feature_id: str
    ) -> str:
        """Emit the body of _prefix_post_install__generated()."""
        if not prefix_groups:
            return ""

        _owner, _repo = git_owner_repo()
        post_install_blocks: list[str] = []
        # cmd_var → first_bin; needs bare-binary fallback when guarded.
        guarded_cmd_vars: dict[str, str] = {}

        for group_id, group_cfg in prefix_groups.items():
            prefix_cfg: dict = group_cfg.get("prefix", {})
            symlink_cfg: dict = group_cfg.get("symlink", {})
            exports_cfg: dict = group_cfg.get("exports", {})
            activation_cfg: dict | None = group_cfg.get("activation")

            option_name: str | None = group_cfg.get("option_name")
            skip_symlink: bool = symlink_cfg.get("skip", False)
            skip_exports: bool = exports_cfg.get("skip", False)
            symlink_root: str = symlink_cfg.get("root", "/usr/local/bin")
            symlink_nonroot: str = symlink_cfg.get("nonroot", "${HOME}/.local/bin")
            bins: list[str] = prefix_cfg.get("bins", [])
            bin_dir: str = prefix_cfg.get("bin_dir", "bin")
            applies_when: list | None = group_cfg.get("applies_when")

            stem = option_name or (f"{group_id}_prefix" if group_id else "prefix")
            opt_prefix = stem
            opt_symlinks = f"{stem}_symlinks"
            opt_exports = f"{stem}_exports"
            opt_discovery = f"{stem}_discovery"

            var_prefix = _opt_to_var(opt_prefix)
            var_symlinks = _opt_to_var(opt_symlinks)
            var_exports = _opt_to_var(opt_exports)
            var_discovery = _opt_to_var(opt_discovery)

            fn_activation_snippet = f"{stem}_activation_snippet"
            var_activations = _opt_to_var(f"{stem}_activations")
            activation_marker = f"{stem} activation ({feature_id})"
            activation_profile_d = f"{_owner}-{_repo}-{feature_id}-{stem}-activation.sh"

            first_bin = bins[0] if bins else ""
            bins_str = " ".join(bins) if bins else ""
            marker = (
                f"{first_bin} PATH ({feature_id})"
                if first_bin
                else f"PATH ({feature_id})"
            )
            if group_id:
                cmd_var = f"_DF_EXPECTED_CMD_{group_id.upper().replace('-', '_')}"
            else:
                cmd_var = "_DF_EXPECTED_CMD"

            # Discovery block
            # Skip entirely when both symlinks and exports are disabled — the call
            # would be a no-op side-effect-wise, and the computed cmd path
            # (prefix/bin/name) would be wrong for installs like nvm that don't
            # place binaries directly under the prefix/bin directory.
            if not (skip_symlink and skip_exports):
                discovery_block = self._build_discovery_block(
                    _DiscoveryParams(
                        stem=stem,
                        var_prefix=var_prefix,
                        bin_dir=bin_dir,
                        var_discovery=var_discovery,
                        var_runtime_path=_opt_to_var("runtime_path"),
                        bins_str=bins_str,
                        var_symlinks=var_symlinks,
                        var_exports=var_exports,
                        marker=marker,
                        first_bin=first_bin,
                        cmd_var=cmd_var,
                        skip_symlink=skip_symlink,
                        skip_exports=skip_exports,
                        symlink_root=symlink_root,
                        symlink_nonroot=symlink_nonroot,
                    )
                )
                post_install_blocks.append(
                    _wrap_applies_when(applies_when, discovery_block)
                )
                if applies_when and first_bin and cmd_var not in guarded_cmd_vars:
                    guarded_cmd_vars[cmd_var] = first_bin

            if activation_cfg:
                activation_block = self._render_inline_template(
                    _TMPL_PREFIX_ACTIVATION,
                    0,
                    stem=stem,
                    marker=activation_marker,
                    profile_d_name=activation_profile_d,
                    snippet_func=fn_activation_snippet,
                    activations_var=var_activations,
                    prefix_var=var_prefix,
                )
                post_install_blocks.append(
                    _wrap_applies_when(applies_when, activation_block)
                )

            write_group_cfg: dict | None = group_cfg.get("write_group")
            if write_group_cfg is not None:
                if group_id:
                    wg_key = f"{group_id}_write_group"
                    wu_key = f"{group_id}_write_users"
                else:
                    wg_key = "write_group"
                    wu_key = "write_users"
                wg_var = _opt_to_var(wg_key)
                wu_var = _opt_to_var(wu_key)
                owner_var_name: str = write_group_cfg.get("owner_var", "")
                owner_expr = (
                    f'"${{{owner_var_name}}}"' if owner_var_name else '"$(id -nu)"'
                )
                write_group_block = self._render_inline_template(
                    _TMPL_WRITE_GROUP,
                    0,
                    wu_var=wu_var,
                    wg_var=wg_var,
                    var_prefix=var_prefix,
                    owner_expr=owner_expr,
                )
                post_install_blocks.append(
                    _wrap_applies_when(applies_when, write_group_block)
                )

        # Bare-binary fallbacks ensure cmd-vars are always set even when an
        # applies_when guard prevented the discovery block from running.
        # The := operator makes each line a no-op when discovery already set the var.
        for _cv, _fb in guarded_cmd_vars.items():
            post_install_blocks.append(f': "${{{_cv}:={_fb}}}"')

        return "\n\n".join(post_install_blocks)

    def _generate_prefix_resolver_calls(self, prefix_groups: dict) -> str:
        """Emit the inline resolver call lines."""
        if not prefix_groups:
            return ""

        resolver_calls: list[str] = []
        for group_id, group_cfg in prefix_groups.items():
            option_name: str | None = group_cfg.get("option_name")
            safe_id = group_id.replace("-", "_")
            if option_name:
                fn_resolve = f"resolve_{option_name}"
            elif group_id:
                fn_resolve = f"resolve_{safe_id}_prefix"
            else:
                fn_resolve = "resolve_prefix"
            resolver_calls.append(fn_resolve)

        return "\n".join(resolver_calls)

    def _generate_dependency_install_functions(
        self, run_deps: dict, build_deps: dict
    ) -> str:
        """Emit _<group>_deps__install() / _<group>_deps__cleanup() helper functions."""
        if not run_deps and not build_deps:
            return ""
        blocks = [
            "# Dependency install helper functions",
        ]
        blocks.extend(
            self._render_inline_template(
                _DEPENDENCY_INSTALL_FN_RUN,
                0,
                group_slug=gn.replace("-", "_"),
                group=gn,
            )
            for gn in run_deps
        )
        blocks.extend(
            self._render_inline_template(
                _DEPENDENCY_INSTALL_FN_BUILD,
                0,
                group_slug=gn.replace("-", "_"),
                group=gn,
            )
            for gn in build_deps
        )
        return "\n".join(blocks)

    def _generate_dependency_install_calls(
        self, run_deps: dict, build_deps: dict
    ) -> str:
        """Emit root-guarded _base_deps__install() calls: build first, then run."""
        parts = []
        if "base" in build_deps:
            parts.append("_dep_install_buildtime_base")
        if "base" in run_deps:
            parts.append("_dep_install_runtime_base")
        if not parts:
            return ""

        return self._render_inline_template(
            _DEPENDENCY_INSTALL_BASE_CALL, 0, calls=" && ".join(parts)
        )

    def _generate_system_requirements_guard(self, sys_req: dict) -> str:
        """Emit platform and root-privilege guards from _system_requirements."""
        if not sys_req:
            return ""

        platforms: list[dict] | None = sys_req.get("platforms")
        root = sys_req.get("root", False)

        blocks: list[str] = []

        if platforms:
            platform_check = _make_match_spec_check(platforms)
            platforms_desc = _match_specs_desc(platforms)
            blocks.append(
                self._render_inline_template(
                    _TMPL_SYS_REQ_PLATFORM_GUARD,
                    0,
                    platform_check=platform_check,
                    platforms_desc=platforms_desc,
                )
            )

        if root is True:
            blocks.append(
                self._render_inline_template(_TMPL_SYS_REQ_ROOT_GUARD_UNCONDITIONAL, 0)
            )
        elif isinstance(root, list) and root:
            platform_check = _make_match_spec_check(root)
            blocks.append(
                self._render_inline_template(
                    _TMPL_SYS_REQ_ROOT_GUARD_CONDITIONAL,
                    0,
                    platform_check=platform_check,
                )
            )

        return "\n\n".join(blocks)

    def _shfmt_format(self, text: str) -> str:
        """Format a bash script fragment through shfmt, respecting .editorconfig.

        Writes to a temp file in the repo root so shfmt finds the .editorconfig
        style settings.  Returns *text* unchanged if shfmt is not on PATH.
        """
        with tempfile.NamedTemporaryFile(
            mode="w",
            suffix=".sh",
            dir=self._repo_dirpath,
            delete=False,
            encoding="utf-8",
        ) as tmp:
            tmp.write(text)
            tmp_path = Path(tmp.name)
        try:
            result = subprocess.run(
                ["shfmt", "-w", str(tmp_path)],
                capture_output=True,
                check=False,
                cwd=self._repo_dirpath,
            )
            if result.returncode == 0:
                return tmp_path.read_text(encoding="utf-8")
            return text
        except FileNotFoundError:
            return text
        finally:
            tmp_path.unlink(missing_ok=True)

    @staticmethod
    def _render_inline_template(template: str, indent: int, **subs: str) -> str:
        """Substitute {key} placeholders in a template, indenting all lines."""
        filled = template.format(**subs)
        indentation = " " * indent
        return "\n".join(
            f"{indentation}{line}" if line.strip() else line
            for line in filled.splitlines()
        ).strip()

    @staticmethod
    def _build_discovery_block(p: _DiscoveryParams) -> str:
        if p.skip_symlink and p.skip_exports:
            comment_suffix = " (symlinks and exports disabled)"
        elif p.skip_symlink:
            comment_suffix = " (no symlinks)"
        else:
            comment_suffix = ""
        args: list[str] = [
            f'--prefix "${{{p.var_prefix}}}"',
            f'--bin-dir "{p.bin_dir}"',
            f'--discovery "${{{p.var_discovery}}}"',
            f'--runtime-path "${{{p.var_runtime_path}}}"',
        ]
        if not p.skip_symlink:
            args += [f'--bins "{p.bins_str}"', f'--symlinks-ref "{p.var_symlinks}"']
            if p.symlink_root != "/usr/local/bin":
                args.append(f'--symlink-root "{p.symlink_root}"')
            if p.symlink_nonroot != "${HOME}/.local/bin":
                args.append(f'--symlink-nonroot "{p.symlink_nonroot}"')
        if not p.skip_exports:
            args += [
                f'--exports-ref "{p.var_exports}"',
                f'--marker "{p.marker}"',
                '--profile-d "${_SHELL_PROFILE_D_FILENAME}"',
            ]
        args += [f'--bin "{p.first_bin}"', f'--cmd-var "{p.cmd_var}"']
        if p.skip_symlink:
            args.append("--no-symlinks")
        if p.skip_exports:
            args.append("--no-exports")
        fn_name = f"{p.stem}_discovery"
        args_lines = "\n".join(
            f"  {arg} \\" if i < len(args) - 1 else f"  {arg}"
            for i, arg in enumerate(args)
        )
        return InstallScriptGenerator._render_inline_template(
            _TMPL_DISCOVERY_BLOCK,
            0,
            stem=p.stem,
            comment_suffix=comment_suffix,
            fn_name=fn_name,
            args_lines=args_lines,
        )


