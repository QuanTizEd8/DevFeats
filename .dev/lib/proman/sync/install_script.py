"""Install script generation for devcontainer features."""

from __future__ import annotations

import re
import shlex
import subprocess
import tempfile
from pathlib import Path

import pyserials

from proman.config import load as load_config
from proman.const import LIFECYCLE_COMMAND_KEYS

# printf '%s' with an accidental real newline before the closing quote (db42a06b bug).
_SPLIT_PRINTF_PERCENT_S_RE = re.compile(r"printf '%s\r?\n\s+'", re.MULTILINE)

# Octal (600, 0644) or symbolic (+x, u+x) modes for _uri.chmod in metadata.
_URI_CHMOD_MODE_RE = re.compile(
    r"^([0-7]{3,4}|\+[rwxXstugo]+|[ugoa]*[-=+][rwxXstugo]+)$"
)


def _uri_chmod_mode(uri_cfg: bool | dict) -> str | None:  # noqa: FBT001
    """Return a chmod mode string from metadata ``_uri``, or None."""
    if not isinstance(uri_cfg, dict):
        return None
    if "chmod" in uri_cfg:
        mode = str(uri_cfg["chmod"])
        if not _URI_CHMOD_MODE_RE.match(mode):
            msg = (
                f"_uri.chmod must be octal (e.g. '600') or symbolic (e.g. '+x'); "
                f"got {mode!r}"
            )
            raise ValueError(msg)
        return mode
    if uri_cfg.get("chmod_exec"):
        return "+x"
    return None


def validate_generated_install_script(script: str) -> None:
    """Reject generated install.bash with broken bash escape sequences."""
    if _SPLIT_PRINTF_PERCENT_S_RE.search(script):
        msg = (
            "generated install.bash contains printf '%s' with a literal line break "
            "before the closing quote; fix Python codegen escaping (use '\\\\n')."
        )
        raise ValueError(msg)


class InstallScriptGenerator:
    """Generate install.bash header blocks from feature metadata."""

    def __init__(self) -> None:
        self._config = load_config()
        self._features_dirpath = self._config.absolute_path("path.features")
        self._template = self._config.absolute_path(
            "path.install_script_template"
        ).read_text()
        self._repo_dirpath = self._config.root_path

    def generate(self, metadata: dict) -> dict[Path, str]:
        """Generate the full install.bash content for a feature.

        The generated content covers everything from the shebang (#!/usr/bin/env bash)
        through the argument-parsing block and default-variable initialisation, and is
        terminated by a single end marker:

            # END OF AUTOGENERATED BLOCK

        Everything after that marker in the file is hand-written implementation code
        and is never touched by this script.

        Options with ``type: array`` in metadata.yaml are treated as bash arrays:

        * CLI branch:      --flag <value>  (repeatable; each invocation appends)
        * env-var branch:  newline-delimited string split into an array
        * Defaults:        empty array  ()

        The ``type: array`` is serialised as ``type: string`` in
        devcontainer-feature.json (handled by metadata.py).
        """
        options: dict = metadata["options"]

        target_file = Path("install.bash")
        body_path = self._features_dirpath / metadata["id"] / target_file
        body = body_path.read_text(encoding="utf-8") if body_path.is_file() else ""

        argparse = self._generate_argparse(options)

        script_parts: dict[str, object] = {
            "usage_options": self._generate_usage_options(options),
            "argparse": argparse,
            "env_vars": self._generate_env_vars(metadata["_env_vars"]),
            "install_contract_vars": self._generate_install_contract_vars(metadata),
            "system_requirements_guard": self._generate_system_requirements_guard(
                metadata.get("_system_requirements", {})
            ),
            "shell_completions_call": self._generate_shell_completions_call(metadata),
            "lifecycle_conf_vars": self._generate_lifecycle_conf_vars(metadata),
            "feature_functions": body,
        }
        template_data = metadata | {"_script": script_parts}
        try:
            script: str = pyserials.update.TemplateFiller().fill(
                data=template_data,
                template=self._template,
            )
        except Exception as e:
            msg = f"Error rendering template: {e}"
            raise ValueError(msg) from e

        validate_generated_install_script(script)
        script_formatted = self._shfmt_format(script)
        return {target_file: script_formatted}

    @staticmethod
    def _generate_env_vars(env_vars: dict) -> dict[str, str]:
        """Emit one ``KEY="value"`` line per entry in metadata ``_env_vars``.

        Keys are emitted verbatim as bash variable names (they ARE the final names).
        Values are double-quoted so ``${HOME}`` and similar expansions still
        work at runtime.
        """
        if not isinstance(env_vars, dict) or not env_vars:
            return {"assignments": "", "unexports": ""}

        lines: list[str] = []
        var_names: list[str] = []
        for key in sorted(env_vars):
            value = env_vars[key]
            if not isinstance(value, str):
                msg = (
                    f"_env_vars['{key}'] must be a string after metadata fill"
                    f" (got {type(value).__name__})"
                )
                raise TypeError(msg)
            escaped = value.replace("\\", "\\\\").replace('"', '\\"')
            lines.append(f'{key}="{escaped}"')
            var_names.append(key)
        return {
            "assignments": "\n".join(lines),
            "unexports": " ".join(var_names),
        }

    def _generate_lifecycle_conf_vars(self, metadata: dict) -> str:
        """Emit the _FEAT_LIFECYCLE_CONF_VARS associative array.

        Scans all lifecycle command entries (onCreateCommand, etc.) and the
        entrypoint for ``_conf_vars`` declarations.  Only emits an entry when
        the matching ``files/<prefix>--<task>.sh`` (or ``files/entrypoint.sh``)
        actually exists in the feature source tree, because the static deploy
        function iterates ``files/`` at runtime and would never reach a missing
        file.
        """
        feature_id: str = metadata["id"]
        files_dir = self._features_dirpath / feature_id / "files"

        # Map from devcontainer lifecycle key to the filename prefix used in files/.
        event_prefix_map: dict[str, str] = {
            "onCreateCommand": "on-create--",
            "updateContentCommand": "update-content--",
            "postCreateCommand": "post-create--",
            "postStartCommand": "post-start--",
            "postAttachCommand": "post-attach--",
        }

        entries: dict[str, str] = {}

        # Lifecycle command entries.
        for lc_key in LIFECYCLE_COMMAND_KEYS:
            lc_block = metadata.get(lc_key, {})
            prefix = event_prefix_map.get(lc_key, "")
            for entry_id, entry in lc_block.items():
                conf_vars: list[str] = entry.get("_conf_vars", [])
                if not conf_vars:
                    continue
                # Derive the short task name from the canonical key.
                # Canonical form: "<lifecycle_key_prefix><task>"  (after normalization).
                lc_key_prefix: str = metadata["_lifecycle_key_prefix"]
                task = entry_id.removeprefix(lc_key_prefix)
                basename = f"{prefix}{task}.sh"
                if (files_dir / basename).exists():
                    entries[basename] = " ".join(conf_vars)

        # Entrypoint.
        ep = metadata.get("entrypoint", {})
        if isinstance(ep, dict):
            ep_conf_vars: list[str] = ep.get("_conf_vars", [])
            if ep_conf_vars and (files_dir / "entrypoint.sh").exists():
                entries["entrypoint.sh"] = " ".join(ep_conf_vars)

        # Build the bash associative array literal.
        if entries:
            return " ".join(
                f'["{k}"]={shlex.quote(v)}' for k, v in sorted(entries.items())
            )
        return ""

    @staticmethod
    def _generate_shell_completions_call(metadata: dict) -> str:
        """Return the __install_shell_completions__ call.

        Wraps it in an applies_when guard if set.
        """
        applies_when = (
            metadata.get("options", {})
            .get("shell_completions", {})
            .get("_applies_when")
        )
        return _wrap_applies_when(applies_when, "__install_shell_completions__")

    def _generate_install_contract_vars(self, metadata: dict) -> dict[str, str]:
        """Emit internal _FEAT_CONTRACT_* and _FEAT_PREFIX_GUARD_* bash variables.

        Variables always emitted (with empty-string defaults when not configured)
        so template references are never unbound:
          _FEAT_CONTRACT_PRIMARY_BIN  — first binary name from _options.prefix.bins
          _FEAT_PREFIX_GUARD_VAR      — bash var name of the applies_when condition
          _FEAT_PREFIX_GUARD_VALS     — space-separated allowed values for that var
          REGISTER_PACKAGE_NAME       — OS package name for dummy PM registration
        """
        prefix_cfg: dict = metadata.get("_options", {}).get("prefix", {})

        scalar_lines: list[str] = []
        scalar_var_names: list[str] = []

        # Primary binary: first bin from _options.prefix.bins.
        bins: list[str] = prefix_cfg.get("bins", [])
        first_bin = bins[0] if bins else ""
        scalar_lines.append(f'_FEAT_CONTRACT_PRIMARY_BIN="{first_bin}"')
        scalar_var_names.append("_FEAT_CONTRACT_PRIMARY_BIN")

        # Prefix guard: derived from _options.prefix.applies_when.
        applies_when: list | None = prefix_cfg.get("applies_when")
        guard_var = ""
        guard_vals = ""
        if applies_when:
            first_cond = applies_when[0] if applies_when else {}
            for opt_key, opt_vals in first_cond.items():
                guard_var = _opt_to_var(opt_key)
                guard_vals = " ".join(str(v) for v in (opt_vals or []))
                break
        scalar_lines.append(f'_FEAT_PREFIX_GUARD_VAR="{guard_var}"')
        scalar_var_names.append("_FEAT_PREFIX_GUARD_VAR")
        scalar_lines.append(f'_FEAT_PREFIX_GUARD_VALS="{guard_vals}"')
        scalar_var_names.append("_FEAT_PREFIX_GUARD_VALS")

        # REGISTER_PACKAGE_NAME: OS package name for dummy registration on
        # non-PM installs. Sourced from _options.method.package.registers_as
        # or _options.method.upstream-package.registers_as (package wins).
        method_meta: dict = metadata.get("_options", {}).get("method", {})
        registers_as: str = (
            method_meta.get("package", {}).get("registers_as", "")
            or method_meta.get("upstream-package", {}).get("registers_as", "")
        ) or ""
        scalar_lines.append(f'REGISTER_PACKAGE_NAME="{registers_as}"')
        scalar_var_names.append("REGISTER_PACKAGE_NAME")

        # When exactly one method is declared, options.method is not generated
        # (no user choice to make). Inject METHOD as a constant so the
        # template's method dispatch still resolves correctly.
        if len(method_meta) == 1:
            scalar_lines.append(f'METHOD="{next(iter(method_meta.keys()))}"')
            scalar_var_names.append("METHOD")

        return {
            "assignments": "\n".join(scalar_lines),
            "unexports": " ".join(scalar_var_names),
        }

    def _generate_usage_options(self, options: dict) -> str:
        """Emit the __usage__() shell function."""
        entries: list[tuple[str, str, str, bool]] = []
        for key, opt in options.items():
            typ = opt["type"]
            flag_str = f"  {_opt_to_flag(key)} {_usage_type_hint(opt)}"
            has_default = "default" in opt
            default = opt.get("default", "")
            is_required = not has_default
            if typ == "boolean":
                disp_default = "true" if default else "false"
            elif typ == "array":
                disp_default = ""
            else:
                disp_default = (
                    str(default) if has_default and default is not None else ""
                )
            desc = _first_sentence(opt.get("description", ""))
            entries.append((flag_str, desc, disp_default, is_required))
        entries.append(("  -h, --help", "Show this help and exit.", "", False))

        col_width = max((len(f) for f, *_ in entries))
        lines = []
        for (flag_str, desc, disp_default, is_required), _ in zip(
            entries,
            options.items(),
            strict=False,
        ):
            padding = " " * (col_width - len(flag_str) + 2)
            if is_required:
                desc_field = f"{desc} (required)"
            elif disp_default:
                desc_field = f'{desc} (default: "{disp_default}")'
            else:
                desc_field = desc
            lines.append(f"{flag_str}{padding}{desc_field}")

        return "\n".join(lines)

    def _generate_argparse(self, options: dict) -> dict[str, str]:
        """Emit the if/else/fi CLI-vs-env-var argument parsing block."""
        cli_inits: list[str] = []
        case_arms: list[str] = []
        env_reads: list[str] = []

        case_arm_indent = 8
        env_read_indent = 4

        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            flag = _opt_to_flag(key)
            # Arrays are intentionally NOT pre-initialised: bash +=() on an unset
            # variable is safe, and pre-setting to () would fool the declare-based
            # default check.
            if typ != "array":
                if "default" not in opt:
                    cli_inits.append(f'    {vname}=""')
                else:
                    rhs = _shell_val(opt["default"], typ)
                    cli_inits.append("    # shellcheck disable=SC2016,SC2088")
                    cli_inits.append(f"    {vname}={rhs}")
            if typ == "array":
                case_arms.append(
                    self._render_inline_template(
                        _ARGPARSE_CASE_ARM_ARRAY,
                        case_arm_indent,
                        flag=flag,
                        var=vname,
                        key=key,
                    ),
                )
                env_reads.append(
                    self._render_inline_template(
                        _ARGPARSE_ENV_READ_ARRAY,
                        env_read_indent,
                        var=vname,
                        key=key,
                    ),
                )
            else:
                case_arms.append(
                    self._render_inline_template(
                        _ARGPARSE_CASE_ARM_SCALAR,
                        case_arm_indent,
                        flag=flag,
                        var=vname,
                        key=key,
                    ),
                )
                env_reads.append(
                    self._render_inline_template(
                        _ARGPARSE_ENV_READ_SCALAR,
                        env_read_indent,
                        var=vname,
                        key=key,
                    ),
                )

        return {
            "cli_inits": "\n".join(cli_inits),
            "case_arms": "\n".join(case_arms),
            "env_reads": "\n".join(env_reads),
            "defaults": self._generate_argparse_defaults(options),
            "normalize_arrays": self._generate_argparse_normalize_arrays(options),
            "normalize_escapes": self._generate_argparse_normalize_escapes(options),
            "uri_resolution": self._generate_argparse_uri_resolution(options),
            "validations": self._generate_argparse_validations(options),
            "unexports": self._generate_argparse_unexports(options),
        }

    def _generate_argparse_uri_resolution(self, options: dict) -> str:
        """Emit URI resolution calls for options marked with `_uri`.

        Best practice: keep runtime logic in shell library functions. The
        generator emits only a compact, data-driven spec string consumed by
        `argparse__resolve_uri_options`.
        """
        spec_lines: list[str] = []
        for key, opt in options.items():
            vname = _opt_to_var(key)
            uri_cfg = opt.get("_uri", False)
            if not uri_cfg:
                continue
            typ = opt.get("type", "string")
            chmod_mode = _uri_chmod_mode(uri_cfg) if isinstance(uri_cfg, dict) else ""
            spec_lines.append(f"{key}\t{vname}\t{typ}\t{chmod_mode}")

        if not spec_lines:
            return ""

        def _escape_ansi_c(s: str) -> str:
            # Escape content for embedding inside bash ANSI-C $'...' strings.
            # Note: we intentionally do NOT escape the "\\n" record separators here;
            # we escape each line first, then join with "\\n" so separators remain
            # a bash newline escape (not a literal backslash-n).
            return s.replace("\\", "\\\\").replace("'", "\\'")

        spec_literal = "\\n".join(_escape_ansi_c(line) for line in spec_lines)
        blocks = [
            f"_DF_URI_SPECS=$'{spec_literal}'",
            'argparse__resolve_uri_options "${_DF_URI_SPECS}"',
            "unset _DF_URI_SPECS",
        ]
        return "\n".join(blocks).strip()

    def _generate_argparse_defaults(self, options: dict) -> str:
        """Emit the '# Apply defaults.' block."""
        blocks = []
        dynamic_blocks = []
        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            if "default" not in opt:
                continue
            if typ == "array":
                raw_default = opt["default"]
                if raw_default == "" or raw_default is None:
                    blocks.append(f"argparse__default_array {vname}")
                else:
                    # Embed the default as an ANSI-C quoted string so newlines
                    # are preserved.
                    escaped = (
                        str(raw_default)
                        .replace("\\", "\\\\")
                        .replace("'", "\\'")
                        .replace("\n", "\\n")
                        .replace("\r", "\\r")
                    )
                    blocks.append(f"argparse__default_array_value {vname} $'{escaped}'")
            else:
                rhs = _shell_val(opt["default"], typ)
                blocks.append("# shellcheck disable=SC2016,SC2088")
                blocks.append(f"argparse__default {vname} {rhs}")
                dynamic_default = opt.get("_default")
                if dynamic_default is not None:
                    dynamic_blocks.append(
                        self._render_inline_template(
                            _ARGPARSE_DEFAULT_DYNAMIC,
                            2,
                            var=vname,
                            rhs=f'"{dynamic_default}"',
                            key=key,
                        ),
                    )

        return "\n".join(blocks + dynamic_blocks)

    def _generate_argparse_normalize_arrays(self, options: dict) -> str:
        """Emit trim/filter calls for every array option after defaults are applied."""
        blocks: list[str] = []
        for key, opt in options.items():
            if opt.get("type") != "array":
                continue
            vname = _opt_to_var(key)
            blocks.append(f"argparse__normalize_array {vname}")
        return "\n".join(blocks)

    def _generate_argparse_normalize_escapes(self, options: dict) -> str:
        r"""Emit argparse__normalize_escapes for `_normalize_escapes: true` options.

        These options use \${VAR} in their defaults to prevent the devcontainer CLI
        from expanding shell expressions at build time; normalization strips the
        leading backslash so downstream code receives a plain ${VAR} expression.
        """
        blocks: list[str] = []
        for key, opt in options.items():
            if not opt.get("_normalize_escapes"):
                continue
            if opt.get("type") == "array":
                msg = (
                    f"Option '{key}': _normalize_escapes is not"
                    " supported for array options."
                )
                raise ValueError(msg)
            vname = _opt_to_var(key)
            blocks.append(f"argparse__normalize_escapes {vname}")
        return "\n".join(blocks)

    def _generate_argparse_validations(self, options: dict) -> str:
        """Emit boolean, integer, enum, and path validation blocks.

        Returns '' if none apply.
        """
        validations: list[str] = []
        for key, opt in options.items():
            vname = _opt_to_var(key)
            typ = opt.get("type", "string")
            if typ == "boolean":
                validations.append(f"argparse__validate_bool {vname}")
            elif typ == "integer":
                validations.append(f"argparse__validate_integer {vname}")
                minimum = opt.get("_minimum")
                if minimum is not None:
                    validations.append(
                        f"argparse__validate_integer_min {vname} {minimum}"
                    )
                maximum = opt.get("_maximum")
                if maximum is not None:
                    validations.append(
                        f"argparse__validate_integer_max {vname} {maximum}"
                    )
            elif opt.get("enum"):
                values = [
                    item["value"] if isinstance(item, dict) else str(item)
                    for item in opt["enum"]
                ]
                fn = (
                    "argparse__validate_enum_array"
                    if typ == "array"
                    else "argparse__validate_enum"
                )
                values_str = " ".join(shlex.quote(v) for v in values)
                validations.append(f"{fn} {vname} {values_str}")
            path_spec = opt.get("_path")
            if path_spec is None:
                continue
            ops = [path_spec] if isinstance(path_spec, str) else path_spec
            if typ == "array":
                validations.extend(
                    f'argparse__validate_path_array {vname} "{op}"' for op in ops
                )
            else:
                validations.extend(
                    f'argparse__validate_path {vname} "{op}"' for op in ops
                )
        return "\n".join(validations)

    def _generate_argparse_unexports(self, options: dict) -> str:
        """Emit the 'declare -g +x' unexport line for all option variables."""
        return " ".join(_opt_to_var(k) for k in options)

    def _generate_system_requirements_guard(self, sys_req: dict) -> str:
        """Emit platform and root-privilege guards from _system_requirements."""
        if not sys_req:
            return ""

        platforms: list[dict] | None = sys_req.get("platforms")
        root = sys_req.get("root", False)

        blocks: list[str] = []

        if platforms:
            spec_args = _match_specs_to_args(platforms)
            blocks.append(f"sys_req__require_platform {spec_args}")

        if root is True:
            blocks.append(
                "# shellcheck disable=SC2119"
                "  # sys_req__require_root takes named args, not positional\n"
                "sys_req__require_root"
            )
        elif isinstance(root, list) and root:
            spec_args = _match_specs_to_args(root)
            blocks.append(
                "# shellcheck disable=SC2119"
                "  # sys_req__require_root takes named args, not positional\n"
                f"sys_req__require_root {spec_args}"
            )

        return "\n\n".join(blocks)

    def _shfmt_format(self, text: str) -> str:
        """Format a bash script fragment through shfmt, respecting .editorconfig.

        Writes to a temp file in the repo root so shfmt finds the .editorconfig
        style settings.  Returns *text* unchanged if shfmt is not on PATH.
        """
        with tempfile.NamedTemporaryFile(
            mode="w",
            suffix=".sh",
            dir=self._repo_dirpath,
            delete=False,
            encoding="utf-8",
        ) as tmp:
            tmp.write(text)
            tmp_path = Path(tmp.name)
        try:
            result = subprocess.run(
                ["shfmt", "-w", str(tmp_path)],
                capture_output=True,
                check=False,
                cwd=self._repo_dirpath,
            )
            if result.returncode == 0:
                return tmp_path.read_text(encoding="utf-8")
            return text
        except FileNotFoundError:
            return text
        finally:
            tmp_path.unlink(missing_ok=True)

    @staticmethod
    def _render_inline_template(template: str, indent: int, **subs: str) -> str:
        """Substitute {key} placeholders in a template, indenting all lines."""
        filled = template.format(**subs)
        indentation = " " * indent
        return "\n".join(
            f"{indentation}{line}" if line.strip() else line
            for line in filled.splitlines()
        ).strip()


# ── Pure utilities ────────────────────────────────────────────────────────────


def _match_specs_to_args(specs: list[dict]) -> str:
    """Build the sys_req__require_* shell arg list (leading -- per group)."""
    parts: list[str] = []
    for spec in specs:
        parts.append("--")
        parts.extend(f"{k}={v}" for k, v in spec.items())
    return " ".join(parts)


def _first_sentence(text: str) -> str:
    """Return the first non-empty line of a description."""
    for raw in text.splitlines():
        stripped = raw.strip()
        if stripped:
            return stripped
    return text.strip()


def _wrap_applies_when(applies_when: list | None, block: str) -> str:
    """Wrap a shell block in a positive if/case guard from an applies_when condition.

    Returns the block unchanged when applies_when is absent or unparseable.
    Input format: [{varname: [val1, val2, ...]}]
    """
    if not applies_when:
        return block
    entry = applies_when[0]
    if not isinstance(entry, dict) or len(entry) != 1:
        return block
    var_name, values = next(iter(entry.items()))
    var = _opt_to_var(var_name)
    if not values:
        return block
    if len(values) == 1:
        return f'if [ "${{{var}}}" = "{values[0]}" ]; then\n{block}\nfi'
    pattern = " | ".join(str(v) for v in values)
    return f'case "${{{var}}}" in {pattern})\n{block}\n;;\nesac'


def _opt_to_var(key: str) -> str:
    """Convert a schema-validated option key to its installer env-var name.

    Option keys are constrained by features/metadata.schema.json to start with an
    ASCII letter and then contain only ASCII letters, digits, underscores, or
    hyphens. Given that contract, env-var generation is just uppercase plus
    '-' -> '_'.
    """
    return key.upper().replace("-", "_")


def _opt_to_flag(key: str) -> str:
    """Convert an option key to its --flag form."""
    return f"--{key}"


def _shell_val(default: object, typ: str) -> str:
    """Format a YAML default value as a shell assignment RHS (non-array types only).

    Non-empty strings are single-quoted so they are stored verbatim without bash
    expanding anything (e.g. '${XDG_CACHE_HOME:-$HOME/.cache}/oh-my-zsh' must
    survive as a literal to be written into user config files and evaluated later
    in the user's own shell).  Defaults that need bash expansion at install time
    must use '_default:' (dynamic defaults) instead of 'default:'.
    """
    if typ == "boolean":
        return "'true'" if default else "'false'"
    if default == "" or default is None:
        return "''"
    val_escaped = str(default).replace("'", "'\\''")
    return f"'{val_escaped}'"


def _usage_type_hint(opt: dict) -> str:
    """Return a concise type hint for the --help usage line."""
    typ = opt["type"]
    enum_raw = opt.get("enum", [])
    enum_values = [
        item["value"] if isinstance(item, dict) else str(item) for item in enum_raw
    ]
    if typ == "boolean":
        return "{true,false}"
    if typ == "integer":
        minimum = opt.get("_minimum")
        maximum = opt.get("_maximum")
        lo = str(minimum) if minimum is not None else ""
        hi = str(maximum) if maximum is not None else ""
        return (
            f"<integer {lo}..{hi}>"
            if minimum is not None or maximum is not None
            else "<integer>"
        )
    if typ == "array":
        if enum_values:
            return "{" + "|".join(enum_values) + "}  (repeatable)"
        return "<value>  (repeatable)"
    if enum_values:
        return "{" + "|".join(enum_values) + "}"
    return "<value>"


